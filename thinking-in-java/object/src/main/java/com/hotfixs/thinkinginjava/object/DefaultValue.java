public class DefaultValue {
    private int intVal;
    private char charVal;

    public static void main(String[] args) {
        DefaultValue defaultValue = new DefaultValue();
        System.out.println(defaultValue.intVal);
        System.out.println(defaultValue.charVal);
    }
}
